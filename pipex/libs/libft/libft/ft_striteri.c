#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;

	i = 0;
	while (*s != '\0')
	{
		f(i, &(*s));
		s++;
		i++;
	}
	while (i != 0)
	{
		s--;
		i--;
	}
}
